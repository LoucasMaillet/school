from tp import sort_insert, sort_select


def tri_par_insertion(liste):
    """trie la liste par l'algorithme tri par insertion
    La fonction ne renvoie rien et la liste passée en argument est triée (tri en place)"""
    sort_insert(liste)


def tri_par_selection(liste):
    """trie la liste par l'algorithme tri par sélection
    La fonction ne renvoie rien et la liste passée en argument est triée (tri en place)"""
    sort_select(liste)


 